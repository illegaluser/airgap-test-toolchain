# ==================================================================================
# File: gitlab_issue_creator.py
# Version: 1.1
#
# [Overview]
# This script handles **stage 3 (auto-register issues into the issue tracker)**
# of the quality analysis pipeline (Phase 3). It reads the LLM analysis output
# (JSONL) from dify_sonar_issue_analyzer.py and automatically creates an issue
# for each entry in the GitLab project's issue tracker.
#
# [Position in the pipeline]
# dify_sonar_issue_analyzer.py (AI analysis)
#       ↓ llm_analysis.jsonl
# >>> gitlab_issue_creator.py (issue registration) <<<
#       ↓ gitlab_issues_created.json (registration result summary)
#
# [Core features]
# 1. Reads the JSONL output line by line and converts each entry to a GitLab issue.
# 2. Issue title is unified to "[severity] issue message" format.
# 3. Replaces SonarQube internal URLs with externally accessible URLs.
# 4. Skips creation if an issue with the same SonarQube key already exists
#    (deduplication).
# 5. Saves create/skip/fail counts to a JSON file for pipeline tracing.
#
# [Run example]
# python3 gitlab_issue_creator.py \
#   --gitlab-host-url http://gitlab:80 \
#   --gitlab-token glpat-xxxxx \
#   --gitlab-project mygroup/myproject \
#   --input llm_analysis.jsonl \
#   --sonar-public-url http://localhost:9000
# ==================================================================================

